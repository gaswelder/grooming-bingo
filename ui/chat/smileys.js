const magick = (str, regexpString, replacement) =>
  str.replace(new RegExp(regexpString, "g"), replacement);

const replaceSkypeSmileys = str =>
  skype.reduce(
    (r, name) =>
      magick(
        r,
        `\\(${name}\\)`,
        `<img class="skype-smiley" src="http://mysmiley.net/skype/Skype_emoji_smiley_${name}_mysmiley.net.gif">`
      ),
    str
  );

const replaceOldSchool = str =>
  Object.entries(smileys).reduce(
    (r, [k, url]) => magick(r, `{${k}}`, `<img src="${url}">`),
    str
  );

export const replaceSmileys = str => replaceSkypeSmileys(replaceOldSchool(str));
